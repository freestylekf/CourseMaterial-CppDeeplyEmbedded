#pragma once

// non-specialized template
template<class T>
struct num_limits_trait
{};

// template specialization for unsigned char
template<>
struct num_limits_trait<unsigned char>
{
	typedef unsigned int safe_addition_type;

	inline static unsigned char min()
	{
		return 0x00;
	}

	inline static unsigned char max()
	{
		return 0xFF;
	}
};

